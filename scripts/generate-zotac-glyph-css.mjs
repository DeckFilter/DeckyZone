import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, extname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, "..");
const sourceRoot = resolve(repoRoot, "src/glyphs/source");
const outputPath = resolve(repoRoot, "src/glyphs/generated/zotacGlyphCss.ts");

const THEME_ASSET_PREFIX = "/themes_custom/handheld-controller-glyphs/assets/";
const cssFiles = [
  "themes/shared/hiding.css",
  "themes/zotac/zone.css",
  "themes/shared/controller-image.css",
  "themes/shared/face-buttons.css",
  "themes/shared/dpad.css",
  "themes/shared/sticks.css",
  "themes/shared/shoulder-buttons.css",
  "themes/shared/l4-r4.css",
  "themes/shared/hide-l5-r5.css",
];

function getAssetMimeType(assetPath) {
  const extension = extname(assetPath).toLowerCase();
  if (extension === ".png") {
    return "image/png";
  }
  if (extension === ".svg") {
    return "image/svg+xml";
  }

  throw new Error(`Unsupported glyph asset type: ${assetPath}`);
}

function resolveAssetPath(themeAssetRelativePath) {
  const directPath = resolve(sourceRoot, "assets", themeAssetRelativePath);
  if (existsSync(directPath)) {
    return directPath;
  }

  // Upstream sticks.css references right-stick directional icons as .png even
  // though the shipped assets are .svg. Normalize that mismatch here so the
  // generated built-in bundle stays aligned with the actual vendored files.
  if (directPath.endsWith(".png")) {
    const svgFallbackPath = directPath.slice(0, -4) + ".svg";
    if (existsSync(svgFallbackPath)) {
      return svgFallbackPath;
    }
  }

  throw new Error(`Missing vendored glyph asset: ${themeAssetRelativePath}`);
}

function assetPathToDataUri(assetPath) {
  const mimeType = getAssetMimeType(assetPath);
  const data = readFileSync(assetPath);
  return `data:${mimeType};base64,${data.toString("base64")}`;
}

function inlineThemeAssets(css) {
  return css.replace(/url\("([^"]+)"\)/g, (_, url) => {
    if (!url.startsWith(THEME_ASSET_PREFIX)) {
      return `url("${url}")`;
    }

    const relativeAssetPath = url.slice(THEME_ASSET_PREFIX.length);
    const assetPath = resolveAssetPath(relativeAssetPath);
    return `url("${assetPathToDataUri(assetPath)}")`;
  });
}

let combinedCss = ":root { --deckyzone-zotac-glyphs-active: 1; --hiding-enabled: 1; }\n";

for (const cssFile of cssFiles) {
  const cssPath = resolve(sourceRoot, cssFile);
  const cssContents = readFileSync(cssPath, "utf8");
  combinedCss += inlineThemeAssets(cssContents) + "\n";
}

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(
  outputPath,
  `// Generated by scripts/generate-zotac-glyph-css.mjs. Do not edit directly.\nexport const ZOTAC_GLYPH_CSS = ${JSON.stringify(combinedCss)};\n`,
);

console.log(`Generated ${outputPath}`);
